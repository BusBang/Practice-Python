# 버블 정렬

# 첫 번째꺼와 두 번째꺼와 비교해 두 번째 것이 더 작으면 교환한다
# if n >= n+1 -> n, n+1 ...
# 0-1 0-2 0-3 1-2 1-3 2-3


def bubbleSort(arr):
    for i in range(0, len(arr)):
        for j in range(i+1, len(arr)):
            if arr[i] > arr[j]:
                temp = arr[i]
                arr[i] = arr[j]
                arr[j] = temp
    return arr


numbers = [7, 3, 2, 9]
# [3, 7, 2, 9]
# [2, 7, 3, 9]
print(bubbleSort(numbers))

print(bubbleSort([7, 6, 4, 3, 9, 8, 10, 1]))
